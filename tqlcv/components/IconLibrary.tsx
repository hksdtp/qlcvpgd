// ============================================
// HELPER FUNCTION
// ============================================

export const getIcon = (
  type: 'department' | 'priority' | 'status',
  value: string,
  solid: boolean = false,
  className?: string
) => {
  if (type === 'department') {
    const IconComponent = solid ? DepartmentIconsSolid[value] : DepartmentIcons[value];
    return IconComponent ? <IconComponent className={className} /> : null;
  }
  if (type === 'priority') {
    const IconComponent = solid ? PriorityIconsSolid[value] : PriorityIcons[value];
    return IconComponent ? <IconComponent className={className} /> : null;
  }
  if (type === 'status') {
    const IconComponent = solid ? StatusIconsSolid[value] : StatusIcons[value];
    return IconComponent ? <IconComponent className={className} /> : null;
  }
  return null;
};

